# CALCULAR EL AREA Y EL PERIMETRO DEL RECTANGULO

# DATOS
base= float(input('ingrese medida de la base del rectangulo:'))
altura= float(input('ingrese medida de la altura del rectangulo:'))

# OPERACION
area = base * altura
perimetro = (base * 2) + (altura * 2)

# RESULTADO
print('El area del rectangulo es: ')
print(area)
print('El perimetro del rectangulo es: ')
print(perimetro)
